namespace Coex.AppLab.Components.WindowsStore.Controls
{
    public sealed partial class FakeProgressDialogControl : UserControl
    {
        public string Message
        {
            get { return (string)GetValue(MessageProperty); }
            set { SetValue(MessageProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Message.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty MessageProperty =
            DependencyProperty.Register("Message", typeof(string), typeof(FakeProgressDialogControl), new PropertyMetadata("Please wait"));

        public FakeProgressDialogControl()
        {
            this.InitializeComponent();
        }

    }
